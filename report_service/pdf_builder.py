# ── Public API ────────────────────────────────────────────

def build_pdf_report(
    client_name: str,
    client_gstin: str,
    filing_period: str,
    ca_name: str,
    firm_name: str,
    total_invoices: int,
    matched_count: int,
    auto_mismatches: list[dict],
    followup_mismatches: list[dict],
    escalation_mismatches: list[dict],
    total_itc_at_risk: float,
) -> bytes:
    """
    Generate a PDF reconciliation report and return it as bytes.

    Args:
        All report data — mismatches already classified by severity
    Returns:
        PDF file contents as bytes (write to file or upload to GCS)
    """
    buffer = io.BytesIO()

    doc = BaseDocTemplate(
        buffer,
        pagesize=A4,
        leftMargin=15*mm, rightMargin=15*mm,
        topMargin=22*mm, bottomMargin=22*mm,
    )
    doc.addPageTemplates([_make_page_template(doc, firm_name)])

    styles = _get_styles()
    story = []

    # ── Cover page ─────────────────────────────────────────
    story.append(Spacer(1, 20*mm))
    story.append(Paragraph("GST Reconciliation Report", styles["title"]))
    story.append(Paragraph(f"{client_name}", styles["subtitle"]))
    story.append(Paragraph(f"GSTIN: {client_gstin}", styles["subtitle"]))
    story.append(Paragraph(f"Filing Period: {filing_period}", styles["subtitle"]))
    story.append(Spacer(1, 4*mm))
    story.append(HRFlowable(width="100%", thickness=1, color=BLUE))
    story.append(Spacer(1, 4*mm))

    # Cover summary box
    cover_data = [
        ["Prepared by", firm_name],
        ["CA Name", ca_name],
        ["Generated on", datetime.now().strftime("%d %B %Y, %I:%M %p")],
        ["Total ITC at Risk", fmt_inr(total_itc_at_risk)],
    ]
    cover_table = Table(cover_data, colWidths=[50*mm, 110*mm])
    cover_table.setStyle(TableStyle([
        ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),
        ("FONTNAME", (1, 0), (1, -1), "Helvetica"),
        ("FONTSIZE", (0, 0), (-1, -1), 10),
        ("ROWBACKGROUNDS", (0, 0), (-1, -1), [WHITE, LIGHT_GREY]),
        ("GRID", (0, 0), (-1, -1), 0.3, MID_GREY),
        ("TOPPADDING", (0, 0), (-1, -1), 6),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
        ("LEFTPADDING", (0, 0), (-1, -1), 8),
        ("TEXTCOLOR", (1, 3), (1, 3), RED),
        ("FONTNAME", (1, 3), (1, 3), "Helvetica-Bold"),
    ]))
    story.append(cover_table)
    story.append(PageBreak())

    # ── Executive Summary ──────────────────────────────────
    story.append(Paragraph("Executive Summary", styles["section"]))
    story.append(HRFlowable(width="100%", thickness=0.5, color=MID_GREY))
    story.append(Spacer(1, 3*mm))

    total_mismatches = len(auto_mismatches) + len(followup_mismatches) + len(escalation_mismatches)
    summary_data = [
        ["Metric", "Count", "Details"],
        ["Total Invoices Analysed", str(total_invoices), "Tally / Zoho Books + GST Portal"],
        ["Clean Matches", str(matched_count), "Fully reconciled — no action required"],
        ["Auto-Resolved (Rounding)", str(len(auto_mismatches)), "Difference ≤ ₹1 — system handled"],
        ["Needs Supplier Follow-up", str(len(followup_mismatches)), "Email sent to supplier(s)"],
        ["Requires CA Review", str(len(escalation_mismatches)), "High ITC risk — action required"],
        ["Total Mismatches", str(total_mismatches), ""],
        ["Total ITC at Risk", fmt_inr(total_itc_at_risk), "Across all unresolved mismatches"],
    ]
    summary_table = Table(summary_data, colWidths=[70*mm, 30*mm, 70*mm])
    summary_table.setStyle(_summary_table_style())

    # Highlight escalation row
    if len(escalation_mismatches) > 0:
        esc_row = 5  # 0=header, 1=invoices, 2=clean, 3=auto, 4=followup, 5=escalation
        summary_table.setStyle(TableStyle([
            ("TEXTCOLOR", (0, esc_row), (-1, esc_row), RED),
            ("FONTNAME", (0, esc_row), (-1, esc_row), "Helvetica-Bold"),
        ]))

    story.append(summary_table)
    story.append(Spacer(1, 8*mm))

    # Severity legend
    legend_data = [
        ["🔴 Escalate", "High ITC risk (≥ ₹5,000) or missing invoice — CA must review"],
        ["🟡 Follow-up", "ITC risk < ₹5,000 — supplier email sent"],
        ["🟢 Auto", "Rounding difference ≤ ₹1 — resolved automatically"],
    ]
    legend_table = Table(legend_data, colWidths=[35*mm, 135*mm])
    legend_table.setStyle(TableStyle([
        ("FONTNAME", (0, 0), (-1, -1), "Helvetica"),
        ("FONTSIZE", (0, 0), (-1, -1), 8),
        ("TOPPADDING", (0, 0), (-1, -1), 3),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 3),
    ]))
    story.append(legend_table)
    story.append(PageBreak())

    # ── Mismatch sections ──────────────────────────────────
    sections = [
        ("🔴 Escalation Mismatches — Requires Immediate CA Action", escalation_mismatches, RED),
        ("🟡 Follow-up Mismatches — Supplier Notified", followup_mismatches, AMBER),
        ("🟢 Auto-Resolved Mismatches", auto_mismatches, GREEN),
    ]

    for section_title, mismatches, colour in sections:
        if not mismatches:
            continue

        story.append(Paragraph(section_title, styles["section"]))
        story.append(HRFlowable(width="100%", thickness=1, color=colour))
        story.append(Spacer(1, 3*mm))

        table_data = [
            ["Invoice No", "Type", "Cause", "ITC Risk", "Action"],
        ]
        for m in mismatches:
            table_data.append([
                Paragraph(str(m.get("invoice_no") or "N/A"), styles["small"]),
                Paragraph(str(m.get("mismatch_type", "")).upper(), styles["small"]),
                Paragraph(str(m.get("cause_reasoning", ""))[:200], styles["small"]),
                Paragraph(fmt_inr(m.get("itc_risk_amount", 0)), styles["small"]),
                Paragraph(str(m.get("recommended_action", ""))[:150], styles["small"]),
            ])

        mismatch_table = Table(
            table_data,
            colWidths=[28*mm, 18*mm, 55*mm, 22*mm, 47*mm],
            repeatRows=1,
        )
        mismatch_table.setStyle(_mismatch_table_style())
        story.append(mismatch_table)
        story.append(Spacer(1, 6*mm))

    # ── Build PDF ──────────────────────────────────────────
    doc.build(story)
    return buffer.getvalue()
