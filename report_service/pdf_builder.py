RED       = colors.HexColor("#dc2626")
AMBER     = colors.HexColor("#d97706")
GREEN     = colors.HexColor("#16a34a")
LIGHT_GREY = colors.HexColor("#f8fafc")
MID_GREY  = colors.HexColor("#e2e8f0")
DARK_GREY = colors.HexColor("#475569")
WHITE     = colors.white
BLACK     = colors.black

SEVERITY_COLOURS = {
    "escalate":  RED,
    "followup":  AMBER,
    "auto":      GREEN,
}

# ── Indian number formatter ────────────────────────────────
def fmt_inr(amount: float) -> str:
    if amount == 0:
        return "₹0.00"
    integer_part, decimal_part = f"{abs(amount):.2f}".split(".")
    n = len(integer_part)
    if n <= 3:
        result = integer_part
    else:
        result = integer_part[-3:]
        remaining = integer_part[:-3]
        while remaining:
            result = remaining[-2:] + "," + result
            remaining = remaining[:-2]
    sign = "-" if amount < 0 else ""
    return f"{sign}₹{result}.{decimal_part}"


# ── Page template with header/footer ──────────────────────

def _make_page_template(doc, firm_name: str):
    """Create a page template with header bar and footer on every page."""
    def _header_footer(canvas, doc):
        canvas.saveState()
        w, h = A4

        # Top header bar
        canvas.setFillColor(NAVY)
        canvas.rect(0, h - 20*mm, w, 20*mm, fill=1, stroke=0)
        canvas.setFillColor(WHITE)
        canvas.setFont("Helvetica-Bold", 11)
        canvas.drawString(15*mm, h - 13*mm, "GST Reconciliation Report")
        canvas.setFont("Helvetica", 9)
        canvas.drawRightString(w - 15*mm, h - 13*mm, firm_name)

        # Footer
        canvas.setFillColor(DARK_GREY)
        canvas.setFont("Helvetica", 8)
        canvas.drawString(15*mm, 10*mm, "Confidential — Generated by GST Reconciliation Agent")
        canvas.drawRightString(w - 15*mm, 10*mm, f"Page {doc.page}")
        canvas.restoreState()

    frame = Frame(
        15*mm, 20*mm,
        A4[0] - 30*mm, A4[1] - 40*mm,
        id="main"
    )
    return PageTemplate(id="main", frames=[frame], onPage=_header_footer)


# ── Styles ─────────────────────────────────────────────────

def _get_styles():
    base = getSampleStyleSheet()
    styles = {
        "title": ParagraphStyle(
            "title", fontName="Helvetica-Bold", fontSize=22,
            textColor=NAVY, spaceAfter=6, alignment=TA_CENTER,
        ),
        "subtitle": ParagraphStyle(
            "subtitle", fontName="Helvetica", fontSize=13,
            textColor=DARK_GREY, spaceAfter=4, alignment=TA_CENTER,
        ),
        "section": ParagraphStyle(
            "section", fontName="Helvetica-Bold", fontSize=13,
            textColor=NAVY, spaceBefore=12, spaceAfter=6,
        ),
        "body": ParagraphStyle(
            "body", fontName="Helvetica", fontSize=9,
            textColor=BLACK, spaceAfter=3, leading=13,
        ),
        "small": ParagraphStyle(
            "small", fontName="Helvetica", fontSize=8,
            textColor=DARK_GREY, spaceAfter=2,
        ),
        "bold": ParagraphStyle(
            "bold", fontName="Helvetica-Bold", fontSize=9,
            textColor=BLACK, spaceAfter=3,
        ),
        "amount": ParagraphStyle(
            "amount", fontName="Helvetica-Bold", fontSize=9,
            textColor=NAVY, alignment=TA_RIGHT,
        ),
        "risk": ParagraphStyle(
            "risk", fontName="Helvetica-Bold", fontSize=9,
            textColor=RED, alignment=TA_RIGHT,
        ),
    }
    return styles


# ── Table style builders ───────────────────────────────────

