"""
report_service/pdf_builder.py
Generates a PDF reconciliation report using ReportLab.

Report structure:
  Page 1 — Cover: Client name, GSTIN, period, generated date
  Page 2 — Executive Summary: Counts, ITC at risk, severity breakdown (table)
  Page 3+ — Mismatch Details: One section per severity group
  Last    — Footer on every page: "Confidential — generated by GST Reconciliation Agent"

Design choices:
  - Uses ReportLab's Platypus (document-layout engine) not canvas directly
  - Styles are defined once and reused — no inline magic numbers
  - Tables use alternating row colours for readability
  - All amounts in Indian number format (₹1,23,456.78)
"""

from __future__ import annotations

import io
import logging
from datetime import date, datetime
from typing import Any

from reportlab.lib import colors
from reportlab.lib.enums import TA_CENTER, TA_LEFT, TA_RIGHT
from reportlab.lib.pagesizes import A4
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import mm
from reportlab.platypus import (
    BaseDocTemplate, Frame, PageTemplate,
    Paragraph, Spacer, Table, TableStyle, HRFlowable, PageBreak,
)

logger = logging.getLogger(__name__)

# ── Colour palette ─────────────────────────────────────────
NAVY      = colors.HexColor("#1a2744")
BLUE      = colors.HexColor("#2563eb")
